num1 = int(input("첫 번째 숫자 입력: "))
num2 = int(input("두 번째 숫자 입력: "))
num3 = int(input("세 번째 숫자 입력: "))

sum_result = num1 + num2 + num3
mul_result = num1 * num2 * num3
sub_result = num1 - num2 - num3
div_result = num1 / num2 / num3

print("덧셈:", sum_result)
print("곱셈:", mul_result)
print("뺄셈:", sub_result)
print("나눗셈:", div_result)