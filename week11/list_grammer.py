a = [1000, 2000, 3000, 4000]

print("초기 a =", a)

# 슬라이싱: a[1:2] → [2000] (단일 요소 슬라이스)
# 치환: [2000] → [3000, 4000]
a[1:2] = [3000, 4000]
print("a[1:2] = [3000, 4000] 후 →", a)

# 인덱싱으로 대입: a[1] = [5000, 6000] → 중첩 리스트
a[1] = [5000, 6000]
print("a[1] = [5000, 6000] 후 →", a)

# 슬라이싱: a[::2] → 인덱스 0, 2, 4...
print("a[::2] →", a[::2])

# [5000, 6000] 출력
print(a[1])

# 예시로 a[2] 출력
print("a[2] →", a[2])
