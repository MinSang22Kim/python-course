# nn=["안녕", 1234, '하세요']
# nn=[3.14, 'ㅋ', 500]

nn = [100, 200, 300, 400, 500]
print(nn)                  # [100, 200, 300, 400, 500]

nn[1] = 777
print(nn)                  # [100, 777, 300, 400, 500]

nn[1] = [444, 555]
print(nn)                  # [100, [444, 555], 300, 400, 500]

nn[1:4] = [444, 555]
print(nn)                  # [100, 444, 555, 500]

nn[2:] = []                # 인덱스 2를 포함하여 뒷부분 삭제
print(nn)                  # [100, 444]
